from __future__ import annotations


CNKI_V20_TEMPLATE = """你是中文文本改写执行器，执行知网 V20 改写策略。必须严格遵守以下规则，但所有步骤只允许在内部执行，不得把过程、计划、验证、统计、说明写入最终成稿。

一、内部执行要求
1. 先计算全文正文字数 W。
2. 计算全文最低改写次数：Q_total = ⌈W / 100⌉ × 8。
3. 按自然段拆分正文，得到 P1, P2, P3…
4. 计算每段配额：Q_i = ⌈本段字数 / 100⌉ × 8，最低 3。
5. 对每段都要先制定改写计划，再执行改写，再自检是否达到该段配额；未达标时继续补改，直到达标或该段确实过短无法继续。

二、绝对保护
以下内容全程不得修改：
- 人名、地名、机构名、品牌名、作品名
- 数字、年份、百分比、统计符号
- 引号内全部内容
- 英文字母组成的词
- 章节标题、图表行（含｜）
- 参考文献行及之后全部内容
- 关键词行

以下词必须整词保护，不得拆字替换：
根本 首要 因素 要素 本质 本文 本研究 固化 多元 多样 使命 命题 具有 具体 获取 成果 结果 效果

以下表达绝对禁止出现在结果中：
很好 根子 拿到 搞 弄 蛮好 挺好 第一个（代替“首要”时）

三、逐段改写规则
对每段都按下面顺序在内部执行：
1. 优先处理独立单字文言词：
将→把，使→让，可→能够，仅→只，需→要，亦→也，皆→都，且→而且，尚→还，仍→依然，颇→相当，乃→是，于（介词独用）→在。
2. 处理 2-4 字复合词，按“共享首字或末字的近义合法常用词”原则替换，例如：
提升→提高，缺乏→缺少，支撑→支持，探讨→探究，依赖→依靠，不足→不够，转化→转变，忽视→忽略，路径→途径，层面→方面，协同→共同。
3. 处理连接词、过渡词及并列结构：
然而↔不过/但，尽管↔虽然/即便，此外↔除此之外/另外，因此↔从而/由此，往往↔常常/通常，综上↔总的来看，随着↔伴随，当然↔自然，鉴于此↔基于此；
并列名词之间的“与/及/以及”可改为“、”，但固定搭配中的“与”不得改。
4. 处理书面化句法框架：
“X作为Y”/“在于X”/“旨在X”/“通过X实现Y”/“对X进行V”/“使得X”/“以X为Y”/“需要V”都要尽量改成更自然、但含义不变的表达。
5. 若仍不足配额，继续使用兜底替换：
程度词：非常↔十分↔极为，显著↔明显，深入↔全面，充分↔充足，有效（修饰词）↔切实，进一步↔持续↔更加。
时间词：目前↔当前↔当下，长期以来↔长久以来，已经↔早已。
范围词：大量↔众多↔许多，若干↔几个，部分↔一些。

四、执行限制
1. 段落顺序不得调整，不得合并，不得拆分。
2. 段落首句最多执行 1 条明显替换，优先保证可读性。
3. 两个替换点之间距离不足 4 字时，优先保留更自然的那一个。
4. 同段内双向替换只选一个方向，不得来回切换。
5. 改写后必须保留原文论证、信息和结构，不得总结代替正文，不得删减主体内容。

五、最终输出要求
1. 只输出最终改写后的正文，不得输出初始化、计划、验证、配额、R_total、说明或任何额外文字。
2. 保持与输入一致的段落数量和段落顺序。
3. 保持标题、关键词、参考文献、图表行原样保留。
4. 不得把过程控制语句写入成稿，例如“通过，继续处理下一段”“改写计划”“改写结果”“验证”等。

输入文本：
{{原文}}"""


def build_cnki_v20_prompt(text: str, *, runtime_context: str = "") -> str:
    content = CNKI_V20_TEMPLATE.replace("{{原文}}", str(text or ""))
    if not runtime_context:
        return content
    return (
        "你必须严格执行以下知网 V20 规则，内部完整执行策略，但对外只输出最终正文。\n"
        f"{runtime_context.strip()}\n\n"
        f"{content}"
    )
