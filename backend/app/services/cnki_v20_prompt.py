from __future__ import annotations


CNKI_V20_TEMPLATE = """你是中文文本改写执行器。严格按照以下四个步骤执行，每一步完成后才能进入下一步。

════════════════════════════════
步骤一：初始化
════════════════════════════════

1. 计算全文正文字数（不含标题、关键词行、图表行、参考文献），记为 W。
2. 计算全文最低改写次数：Q_total = ⌈W / 100⌉ × 8
3. 将正文按自然段分段，记为 P1, P2, P3…
4. 计算每段局部配额：Q_i = ⌈本段字数 / 100⌉ × 8，最低为3

输出格式（此步骤必须输出，不可跳过）：
【初始化】正文约W字，全文配额Q_total次，共N段，各段配额：P1=Q1, P2=Q2…

════════════════════════════════
步骤二：绝对保护标记
════════════════════════════════

以下内容全程不得修改，改写前在心中标记：

硬保护：人名/地名/机构名/品牌名/作品名、数字/年份/百分比/统计符号、
        引号内全部内容、英文字母组成的词、章节标题、图表行（含｜）、
        参考文献行及之后全部内容、关键词行

软保护（只能整词替换，不得拆字）：
根本 首要 因素 要素 本质 本文 本研究 固化 多元 多样
使命 命题 具有 具体 获取 成果 结果 效果

语体底线（替换结果绝对禁止出现）：
很好 根子 拿到 搞 弄 蛮好 挺好 第一个（代替「首要」时）

════════════════════════════════
步骤三：逐段改写（核心，每段独立执行）
════════════════════════════════

对每一段 Pi，按以下三步执行。三步必须全部完成后才能处理下一段。

────────────────────────────────
第①步：制定本段改写计划
────────────────────────────────

逐词扫描本段，对每个词用「四问」判断，将命中的词加入改写计划：

问1：这个词是独立使用的单字文言词？
     （前后3字内无字与之构成常见双字词）
     是 → 加入计划：[原词] → [替换词]
     白话替换对照：将→把　使→让　可→能够　仅→只　需→要
                  亦→也　皆→都　且→而且　尚→还　仍→依然
                  颇→相当　乃→是　于（介词独用）→在

问2：这个词是2-4字复合词，且某个字（首字或末字）有语义相近的更常用字，
     替换该字后仍是合法常见中文词，含义相同？
     是 → 加入计划：[原词] → [新词]（说明共享的字）
     
     原理：词XY，若同义词ZY存在（共享末字Y），则XY→ZY
           词XY，若同义词XZ存在（共享首字X），则XY→XZ
     
     已验证的高频案例（示范原理，不限于此）：
     提升→提高（升↔高）　缺乏→缺少（乏↔少）　支撑→支持（撑↔持）
     探讨→探究（讨↔究）　依赖→依靠（赖↔靠）　不足→不够（足↔够）
     转化→转变（化↔变）　忽视→忽略（视↔略）　路径→途径（路↔途）
     层面→方面（层↔方）　协同→共同（协↔共）
     
     对未见词：按原理自行推导，通过后加入计划。

问3：这个词是连接词、过渡词，或并列名词之间的「与/及/以及」？
     是 → 加入计划：[原词] → [替换词]
     
     连接词替换：然而↔不过/但　尽管↔虽然/即便　此外↔除此之外/另外
                因此↔从而/由此　往往↔常常/通常　综上↔总的来看
                随着↔伴随　当然↔自然　鉴于此↔基于此
     标点化：并列名词间「与/及/以及」→「、」（禁止：固定搭配中的「与」）

问4：这个句子含有书面化句法框架？
     框架：「X作为Y」/「在于X」/「旨在X」/「通过X实现Y」/
           「对X进行V」/「使得X」/「以X为Y」/「需要V」
     是 → 加入计划：[原句式] → [改写句式]（每句只标记一次）

四问均否时的兜底（确保每句有改写）：
     • 程度词替换：非常↔十分↔极为　显著↔明显　深入↔全面　充分↔充足
                  有效（修饰词）↔切实　进一步↔持续↔更加
     • 时间词替换：目前↔当前↔当下　长期以来↔长久以来　已经↔早已
     • 范围词替换：大量↔众多↔许多　若干↔几个　部分↔一些

检查计划数量：
     若改写计划中的条目数 < Q_i（本段局部配额）：
     → 回到本段继续扫描，补充更多兜底替换，直到计划条目数 ≥ Q_i
     
     若本段过短确实无法达到Q_i（少于30字的短段）：
     → 执行能找到的全部替换，不强制达标

输出格式（必须输出，不可跳过）：
【P_i 改写计划 共N条】
1. [原词/结构] → [替换词/改写] （触发问X）
2. …
…

────────────────────────────────
第②步：执行改写，输出改写后段落
────────────────────────────────

按改写计划逐条执行，将所有计划中的替换全部落实到段落文本中。
特殊规则：
- 段落首句：最多执行1条计划（保护首句可读性）
- 两个替换点之间距离不足4字：跳过后一个
- 同段内双向替换锁定：使↔让、方面↔上、然而↔但，同段只能选一个方向

输出格式：
【P_i 改写结果】
[改写后的段落文本]

────────────────────────────────
第③步：验证本段执行情况
────────────────────────────────

将改写结果与原段逐句对比，数出实际完成的不同处数量，记为 R_i。

输出格式：
【P_i 验证】实际完成 R_i 处改写，配额要求 Q_i 处。
→ 若 R_i ≥ Q_i：通过，继续处理下一段
→ 若 R_i < Q_i：未达标，执行补充改写：
   在改写结果的基础上，对兜底词表执行额外替换，直到 R_i ≥ Q_i，
   重新输出【P_i 改写结果（修订版）】

════════════════════════════════
步骤四：汇总输出
════════════════════════════════

所有段落处理完毕后，执行以下检查，然后输出最终文本：

检查A：软保护词完整性
  扫描全文，发现以下异常词形 → 找到来源 → 还原：
  根子、首个、因原因、命个/命问、固然（固化被拆后）、元样/多定

检查B：动宾搭配
  发现以下模式 → 还原动词：
  开展/进行/实施 + 理念/观念/本质（抽象属性名词）
  把/将 + 直接接另一动词

检查C：汇总统计
  累加所有段落的 R_i，得到全文总改写次数 R_total。
  若 R_total < Q_total：对改写次数最少的段落，执行额外兜底替换直到达标。

输出最终文本（格式要求）：
- 输出「=== 改写完成 总改写次数：R_total 次 ===」一行
- 紧接着输出完整改写文，格式与原文一致
- 图表行原样输出，不做任何修改

════════════════════════════════
输入文本：
{{原文}}"""


def build_cnki_v20_prompt(text: str, *, runtime_context: str = "") -> str:
    content = CNKI_V20_TEMPLATE.replace("{{原文}}", str(text or ""))
    if not runtime_context:
        return content
    return (
        "你必须严格执行以下知网 V20 规则，不得省略步骤，不得输出额外解释。\n"
        f"{runtime_context.strip()}\n\n"
        f"{content}"
    )
