from __future__ import annotations


VIP_W4_TEMPLATE = """你是维普风格中文文本改写执行器。严格按照以下四个步骤执行，每一步完成后才能进入下一步。

════════════════════════════════
步骤一：初始化
════════════════════════════════

1. 计算全文正文字数（不含标题、关键词行、图表行、参考文献），记为 W。
2. 计算全文最低改写次数：Q_total = ⌈W / 100⌉ × 5
3. 将正文按自然段分段，记为 P1, P2, P3…
4. 计算每段局部配额：Q_i = ⌈本段字数 / 100⌉ × 5，最低3次

输出格式（此步骤必须输出，不可跳过）：
【初始化】正文约W字，全文配额Q_total次，共N段，各段配额：P1=Q1, P2=Q2…

════════════════════════════════
步骤二：绝对保护标记
════════════════════════════════

以下内容全程不得修改：
· 人名/地名/机构名/品牌名/作品名
· 数字、年份、百分比、统计符号
· 引号内全部内容
· 英文字母组成的词
· 章节标题、图表行（含｜）
· 参考文献行及之后全部内容
· 关键词行

语体底线（替换结果绝对禁止出现）：
拿 搞 弄 蛮好 挺好 非常棒 很厉害

════════════════════════════════
步骤三：逐段改写（核心，每段独立执行）
════════════════════════════════

对每一段 Pi，按以下三步执行。三步必须全部完成后才能处理下一段。

────────────────────────────────
第①步：制定本段改写计划
────────────────────────────────

逐词/逐句扫描本段，对每个词和每个句子用「四问」判断，
将命中的改写项加入计划列表：

问1：这个词是评价性修饰词，且可以用更中性/通俗的词替换？
     判断信号：「重要/核心/关键」作修饰语，「系统化/精准/深刻」作修饰语，
              「得天独厚/丰厚/卓越/宝贵」等偏书面的评价词
     是 → 加入计划：[原词] → [替换词]
     
     生成规则：找一个正式度比原词低一档的同义词。
     校准锚（示范，不限于此）：
     重要（修饰词）→ 主要　核心（修饰词）→ 主要　关键（修饰词）→ 主要
     系统化→ 整体　精准→ 准确　深刻→ 明显　得天独厚→ 有利/良好
     宝贵→ 重要/珍贵　丰厚→ 丰富　首要→ 主要　科学（修饰词）→ 合理

问2：这个词是学术结构名词，可以替换为更日常的同义词？
     判断信号：描述某种机制/方式/场所/框架/全貌的抽象名词，
              在普通日常文章里不会出现
     是 → 加入计划：[原词] → [替换词]
     
     生成规则：找一个在日常文章中更常见的同义词替换。
     校准锚（示范，不限于此）：
     机制→ 方式/方法　路径→ 途径/方式　体系→ 模式/系统
     场域→ 空间/场所　图谱→ 模式/全貌　维度→ 方面
     层面→ 方面　困境→ 问题/困难　瓶颈→ 问题/阻碍
     载体→ 平台/形式　阵地→ 场所（非军事语境）

问3：这个词是书面连接词、副词或介词，可以替换为更口语化的同义表达？
     判断信号：然而/尽管/以及/亟待/旨在/以期/较为/仍较/颇为等
     是 → 加入计划：[原词] → [替换词]
     
     生成规则：找一个正式度明显低于原词的同义表达。
     校准锚（示范，不限于此）：
     连接词：然而→ 不过/但是　尽管→ 虽然　此外→ 另外　以及→ 和
             与（连词）→ 和　当然→ 自然　从而→ 因此/所以
     副词：  较为→ 比较　仍较→ 仍然比较　亟待→ 需要　仍→ 仍然
             往往→ 经常　尤其→ 特别　旨在→ 希望/目的是
             以期→ 希望　颇为→ 相当/比较　当前→ 目前
     动词：  具备→ 拥有　具有→ 拥有/有　孕育了→ 留下了
             赋予→ 给予　涌现于→ 出现在　承载→ 包含/记录

问4：这个句子含有复杂的书面化句法框架，可以简化？
     判断信号：
     T1「X作为Y」（判断句式）→ 替换为「X是Y」
     T2「X在于Y」→ 替换为「X是Y」
     T3「一是…；二是…；三是…」（分号连缀）→ 改为「第一…。二是…。第三…」
     T4「X大致可分为N类」→ 改为「X主要分为N种类型」
     T5「以X为核心/中心/主体」→ 改为「以X为主」/「主要针对X」
     T6「如何将X转化为Y」→ 改为「怎样把X变成Y」
     是 → 加入计划：[原句式关键词] → [简化方式]（每句只标记一次）

四问均否时的兜底（确保每句有改写）：
     • 「重要的」→「主要的」（最高频，任何时候都可用）
     • 当前→目前　机制→方式　路径→途径（高频固定替换）
     • 过渡句的「此外/然而」→「另外/不过」
     • 书面副词：较为→比较　仍→仍然　尤其→特别

检查计划数量：
     若改写计划中的条目数 < Q_i（本段局部配额）：
     → 回到本段继续扫描，按四问补充更多候选，直到计划条目数 ≥ Q_i
     → 若本段确实词汇密度不足，短段（<30字）可不强制达标

输出格式（必须输出，不可跳过）：
【P_i 改写计划 共N条】
1. [原词/结构] → [替换词/简化] （触发问X）
2. …
…

────────────────────────────────
第②步：执行改写，输出改写后段落
────────────────────────────────

按改写计划逐条执行，将所有计划中的改写全部落实到段落文本中。

执行约束：
· 每条计划必须落实，不能跳过已承诺的改写项
· 改写后字数与本段原文字数相差不超过 ±5%
· 若某条计划执行后产生动宾搭配错误（如「进行理念」）→ 还原该条，从备选词中换一个
· 若某条计划执行后出现口语词（拿/搞/弄/蛮好/挺好）→ 还原该条，换更书面的替代词

输出格式：
【P_i 改写结果】
[改写后的段落文本]

────────────────────────────────
第③步：验证本段执行情况
────────────────────────────────

将改写结果与原段逐句对比，数出实际与原文不同的处数，记为 R_i。

输出格式：
【P_i 验证】实际完成 R_i 处改写，配额要求 Q_i 处，字数变化 ±X字。
→ 若 R_i ≥ Q_i 且字数变化在 ±5% 内：通过，继续处理下一段
→ 若 R_i < Q_i：未达标，补充改写：
   在改写结果基础上，继续用四问的兜底规则执行额外替换，
   直到 R_i ≥ Q_i，重新输出【P_i 改写结果（修订版）】
→ 若字数变化超过 ±5%：调整改写力度，还原部分句式改写（T3/T4类），
   以词汇替换为主，重新输出修订版

════════════════════════════════
步骤四：汇总输出
════════════════════════════════

所有段落处理完毕后，执行以下检查，然后输出最终文本：

检查A：全文「重要」使用情况
  若「重要」作为修饰词出现在改写文中，且该处在改写计划中被列为R1候选，
  但未被替换 → 确认并执行替换。
  （「重要」→「主要」是全文最高频替换，不可遗漏）

检查B：动宾搭配
  扫描改写文，发现「进行/实施/开展 + 理念/观念/本质」→ 还原动词。

检查C：汇总统计
  累加所有段落的 R_i，得到 R_total。
  若 R_total < Q_total：对 R_i 最小的段落，执行额外替换直到全文达标。

输出最终文本：
- 输出「=== 改写完成 总改写次数：R_total 次 ===」一行
- 紧接着输出完整改写文，格式与原文一致
- 图表行原样输出

════════════════════════════════
输入文本：
{{原文}}"""


def build_vip_w4_prompt(text: str, *, runtime_context: str = "") -> str:
    content = VIP_W4_TEMPLATE.replace("{{原文}}", str(text or ""))
    if not runtime_context:
        return content
    return (
        "你必须严格执行以下维普 W4 规则，不得省略步骤，不得输出额外解释。\n"
        f"{runtime_context.strip()}\n\n"
        f"{content}"
    )
