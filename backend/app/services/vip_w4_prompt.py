from __future__ import annotations


VIP_W4_TEMPLATE = """你是维普风格中文文本改写执行器，执行维普 W4 改写策略。必须严格遵守以下规则，但所有步骤只允许在内部执行，不得把过程、计划、验证、统计、说明写入最终成稿。

一、内部执行要求
1. 先计算全文正文字数 W。
2. 计算全文最低改写次数：Q_total = ⌈W / 100⌉ × 5。
3. 按自然段拆分正文，得到 P1, P2, P3…
4. 计算每段配额：Q_i = ⌈本段字数 / 100⌉ × 5，最低 3。
5. 每段都要先制定改写计划，再执行改写，再自检是否达到该段配额与字数约束；未达标时继续补改。

二、绝对保护
以下内容全程不得修改：
- 人名、地名、机构名、品牌名、作品名
- 数字、年份、百分比、统计符号
- 引号内全部内容
- 英文字母组成的词
- 章节标题、图表行（含｜）
- 参考文献行及之后全部内容
- 关键词行

以下表达绝对禁止出现在结果中：
拿 搞 弄 蛮好 挺好 非常棒 很厉害

三、逐段改写规则
对每段都按下面顺序在内部执行：
1. 先处理评价性修饰词，统一向更中性、通俗的方向替换，例如：
重要/核心/关键（作修饰语）→主要，系统化→整体，精准→准确，深刻→明显，得天独厚→有利/良好，宝贵→重要/珍贵，丰厚→丰富，首要→主要，科学（修饰词）→合理。
2. 再处理学术结构名词，改成更日常的表达，例如：
机制→方式/方法，路径→途径/方式，体系→模式/系统，场域→空间/场所，图谱→模式/全貌，维度→方面，层面→方面，困境→问题/困难，瓶颈→问题/阻碍，载体→平台/形式，阵地→场所（非军事语境）。
3. 处理书面连接词、副词、介词和常见书面动词，改成正式度更低、但语义不变的表达，例如：
然而→不过/但是，尽管→虽然，此外→另外，以及→和，与（连词）→和，当然→自然，从而→因此/所以；
较为→比较，仍较→仍然比较，亟待→需要，仍→仍然，往往→经常，尤其→特别，旨在→希望/目的是，以期→希望，颇为→相当/比较，当前→目前；
具备→拥有，具有→拥有/有，孕育了→留下了，赋予→给予，涌现于→出现在，承载→包含/记录。
4. 处理复杂书面句式，改成更自然的表达，例如：
“X作为Y”→“X是Y”，“X在于Y”→“X是Y”，“一是…；二是…；三是…”改成更自然的顺序句，
“X大致可分为N类”→“X主要分为N种类型”，“以X为核心/中心/主体”→“以X为主”或“主要针对X”，“如何将X转化为Y”→“怎样把X变成Y”。
5. 若仍不足配额，继续优先使用这些高频替换：
“重要的”→“主要的”，当前→目前，机制→方式，路径→途径，此外/然而→另外/不过，较为→比较，仍→仍然，尤其→特别。

四、执行限制
1. 段落顺序不得调整，不得合并，不得拆分。
2. 每段改写后字数尽量贴近原段，不得通过大幅删减来完成改写。
3. 不得把摘要式概括、结论式压缩代替原正文。
4. 若改写后出现动宾搭配错误或口语化错误，必须在内部修正后再输出。

五、最终输出要求
1. 只输出最终改写后的正文，不得输出初始化、计划、验证、配额、R_total、说明或任何额外文字。
2. 保持与输入一致的段落数量和段落顺序。
3. 保持标题、关键词、参考文献、图表行原样保留。
4. 不得把过程控制语句写入成稿，例如“通过，继续处理下一段”“改写计划”“改写结果”“验证”等。

输入文本：
{{原文}}"""


def build_vip_w4_prompt(text: str, *, runtime_context: str = "") -> str:
    content = VIP_W4_TEMPLATE.replace("{{原文}}", str(text or ""))
    if not runtime_context:
        return content
    return (
        "你必须严格执行以下维普 W4 规则，内部完整执行策略，但对外只输出最终正文。\n"
        f"{runtime_context.strip()}\n\n"
        f"{content}"
    )
